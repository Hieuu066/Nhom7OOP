    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String account = jTextField1.getText();
        double money = Double.parseDouble(jTextField2.getText());

        for(Account acc : BankData.accounts){

            if(acc.accountNumber.equals(account)){

                if(acc.balance >= money){
                    acc.balance -= money;
                }else{
                    javax.swing.JOptionPane.showMessageDialog(this,"Không đủ tiền");
                }

                break;
            }
        }
        String type = "Rút tiền";
        String time = java.time.LocalDateTime.now().toString();

        Transaction t = new Transaction(account, type, money, time);
        BankData.transactions.add(t);
    }//GEN-LAST:event_jButton2ActionPerformed

